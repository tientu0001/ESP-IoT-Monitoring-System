void E2P_ResetE2p_data()
{
#ifdef ESP_DEBUG_E2P
    Serial.printf("E2P_ResetE2p_data\r\n");
#endif

    e2p_data.write(E2PE_ADR_INIT_DATA, E2PE_INIT_CODE_DATA);

    e2p_data.write_32(E2PE_ADR_MC_DAPOT, 0);   // power ontime
    e2p_data.write_32(E2PE_ADR_MC_HANDLE, 0);  // handling time
    e2p_data.write_32(E2PE_ADR_MC_DAMRT, 0);   // motor runtime
    e2p_data.write_16(E2PE_ADR_MC_CT, 0);      // cycle time
    e2p_data.write_32(E2PE_ADR_MC_NUM_STI, 0); // sum of stitch
    e2p_data.write_32(E2PE_ADR_MC_NUM_TRI, 0); // sum of trim

    reset_production();

    e2p_data.commit_flag = true;

    debug.msg_debug.concat(" --/ rst_data");
    debug.send_debug = true;
}

void chkAndonMode()
{
    uint16_t usModeEEP = e2p_info.read_16(E2PE_ADR_BREAK_MODE);
    if (usModeEEP != MOD_BRK_OFF)
    {
        for (uint8_t i = 0; i < LEN_BREAK_REASON; i++)
        {
            uint16_t usMode = MOD_BRK_MACHINE << (i * 4);
            if (usMode == usModeEEP)
            {
                lcd.list_index = i;
                lcd.disp_andon_sub();
                break;
            }
        }
    }

    oper.bSendAndon = true;
}

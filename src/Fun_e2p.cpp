void E2P_ResetE2p_info()
{
#ifdef ESP_DEBUG_E2P
    Serial.printf("E2P_ResetE2p_info\r\n");
#endif

    e2p_info.write(E2PE_ADR_INIT_INFO, E2PE_INIT_CODE_INFO);

    e2p_info.write(E2PE_ADR_INDX_PLAN, 0);   // current index  plan
    e2p_info.write(E2PE_ADR_DAI_OUT_RST, 1); // daily output reset, default 1

    e2p_info.write(E2PE_ADR_LEN_BLA_NFC, 0);     // nfc blank uid nfc (classic 4, mifare ultralight 7)
    e2p_info.write(E2PE_ADR_SEN_BLA_NFC, 0);     // flag send blank uid nfc to server
    e2p_info.write(E2PE_ADR_INOUT_MODE, 0);      // disable in/out iot mode
    e2p_info.write(E2PE_ADR_IGNORE_SEN0_TEA, 0); // disable ignore sen0 teaching sequence
    e2p_info.write(E2PE_ADR_DIR_ANDON_MC, 0);    // disable dir. andon mc

    e2p_info.write_16(E2PE_ADR_OUT_SCALE, 1);                               // output scale
    e2p_info.write_16(E2PE_ADR_BREAK_MODE, MOD_BRK_OFF);                    // current andon mode
    e2p_info.write_16(E2PE_ADR_TEA_DLY_SEN0, Fun_teaSeq::TEA_DEF_DLY_SEN0); // current andon mode

    // location
    e2p_info.write_16(E2PE_ADR_POSITION, 0);
    e2p_info.StringToE2P(E2PE_ADR_FACTORY, E2PE_LEN_FACTORY, String("-"));
    e2p_info.StringToE2P(E2PE_ADR_LINE, E2PE_LEN_LINE, String("-"));
    // machine
    e2p_info.StringToE2P(E2PE_ADR_MC_ID, E2PE_LEN_MC_ID, String("-"));
    e2p_info.StringToE2P(E2PE_ADR_MC_SUPY, E2PE_LEN_MC_SUPY, String("-"));
    // worker
    e2p_info.StringToE2P(E2PE_ADR_WRK_ID, E2PE_LEN_WRK_ID, String("-"));
    e2p_info.StringToE2P(E2PE_ADR_WRK_NA, E2PE_LEN_WRK_NA, String("-"));
    e2p_info.StringToE2P(E2PE_ADR_WRK_SKILL, E2PE_LEN_WRK_SKILL, String("-"));
    // process
    e2p_info.StringToE2P(E2PE_ADR_STYLE, E2PE_LEN_STYLE, String("-"));
    e2p_info.StringToE2P(E2PE_ADR_PROC, E2PE_LEN_PROC, String("-"));
    e2p_info.StringToE2P(E2PE_ADR_PROC_SEQ, E2PE_LEN_PROC_SEQ, String("-"));

    rst_allTeachingSequence(); // reset teaching

    e2p_info.commit_flag = true;

    debug.msg_debug.concat(" --/ rst_info");
    debug.send_debug = true;
}
/////////////////////////////////////////////////////////////////////////////////////
void E2P_UpdateE2pVal()
{
    //////////////////////////////////////////
    get_uid_blank();
    check_uid_blank_problem();
    //////////////////////////////////////////
    oper.current_idx_plan = e2p_info.read(E2PE_ADR_INDX_PLAN);
    if (oper.current_idx_plan >= LEN_JSON_OBJECT_PLAN)
    {
        oper.current_idx_plan = 0;
        e2p_info.write(E2PE_ADR_INDX_PLAN, oper.current_idx_plan);
    }

    //
    oper.daily_product_reset = e2p_info.read(E2PE_ADR_DAI_OUT_RST);
    oper.scale = e2p_info.read_16(E2PE_ADR_OUT_SCALE);

    // location
    oper.factory = e2p_info.E2PtoString(E2PE_ADR_FACTORY, E2PE_LEN_FACTORY);
    oper.line = e2p_info.E2PtoString(E2PE_ADR_LINE, E2PE_LEN_LINE);
    oper.position = e2p_info.read_16(E2PE_ADR_POSITION);
    // machine
    oper.machine_id = e2p_info.E2PtoString(E2PE_ADR_MC_ID, E2PE_LEN_MC_ID);
    // worker
    oper.worker_id = e2p_info.E2PtoString(E2PE_ADR_WRK_ID, E2PE_LEN_WRK_ID);
    oper.worker_name = e2p_info.E2PtoString(E2PE_ADR_WRK_NA, E2PE_LEN_WRK_NA);
    oper.worker_skill = e2p_info.E2PtoString(E2PE_ADR_WRK_SKILL, E2PE_LEN_WRK_SKILL);
    // process
    oper.style = e2p_info.E2PtoString(E2PE_ADR_STYLE, E2PE_LEN_STYLE);
    oper.process = e2p_info.E2PtoString(E2PE_ADR_PROC, E2PE_LEN_PROC);

    // teaching
    tea.delay_sen0 = e2p_info.read_16(E2PE_ADR_TEA_DLY_SEN0);
    if (tea.delay_sen0 > Fun_teaSeq::TEA_MAX_DLY_SEN0) // more than 2000ms
    {
        tea.delay_sen0 = Fun_teaSeq::TEA_DEF_DLY_SEN0; // 10ms
        e2p_info.write_16(E2PE_ADR_TEA_DLY_SEN0, tea.delay_sen0);
    }
    read_allTeachingSequence();

    // in/out iot mode
    if (e2p_info.read(E2PE_ADR_INOUT_MODE) == 1)
        oper.in_out_iot_mode_enable = true;
    else
        oper.in_out_iot_mode_enable = false;

    oper.in_out_input_mode = 1;

    // ignore sen0 teaching sequence
    if (e2p_info.read(E2PE_ADR_IGNORE_SEN0_TEA) == 1)
        tea.ignore_sen0_teaseq = true;
    else
        tea.ignore_sen0_teaseq = false;

    // dir. andon mc mode
    if (e2p_info.read(E2PE_ADR_DIR_ANDON_MC) == 1)
        oper.dir_andon_mc_mode = true;
    else
        oper.dir_andon_mc_mode = false;

    //////////////////////////////////////////
    // page data
    // product data
    oper.output = e2p_data.read_32(oper.list_addr_output[oper.current_idx_plan]);
    oper.defect = e2p_data.read_32(oper.list_addr_output[oper.current_idx_plan] + E2PE_LEN_OUTPUT);
    oper.target = e2p_data.read_32(oper.list_addr_output[oper.current_idx_plan] + E2PE_LEN_OUTPUT + E2PE_LEN_DEFECT);
    // machine time
    oper.power_ontime = e2p_data.read_32(E2PE_ADR_MC_DAPOT); // power on time
    if (oper.power_ontime > 90000)
    {
        // reset m/c time
        oper.power_ontime = 0;
        oper.motor_runtime = 0;
        oper.handling_time = 0;
        oper.cycle_time = 0;
        oper.sumof_stitch = 0;
        oper.sumof_trim = 0;

        e2p_data.write_32(E2PE_ADR_MC_DAPOT, oper.power_ontime);
        e2p_data.write_32(E2PE_ADR_MC_DAMRT, oper.motor_runtime);
        e2p_data.write_32(E2PE_ADR_MC_HANDLE, oper.handling_time);
        e2p_data.write_32(E2PE_ADR_MC_CT, oper.cycle_time);
        e2p_data.write_32(E2PE_ADR_MC_NUM_STI, oper.sumof_stitch);
        e2p_data.write_32(E2PE_ADR_MC_NUM_TRI, oper.sumof_trim);
    }

    oper.power_time_line = oper.power_ontime;
    oper.motor_runtime = e2p_data.read_32(E2PE_ADR_MC_DAMRT);  // motor runtime
    oper.handling_time = e2p_data.read_32(E2PE_ADR_MC_HANDLE); // handling time
    oper.cycle_time = e2p_data.read_32(E2PE_ADR_MC_CT);        // cycle time
    oper.sumof_stitch = e2p_data.read_32(E2PE_ADR_MC_NUM_STI); // number of stitch
    oper.sumof_trim = e2p_data.read_32(E2PE_ADR_MC_NUM_TRI);   // number of trim

    //////////////////////////////////////////
    // page network
    // network
    wifi.sSsid = e2p_network.E2PtoString(E2PE_ADR_SSID, E2PE_LEN_SSID);
    wifi.sPassword = e2p_network.E2PtoString(E2PE_ADR_PASS, E2PE_LEN_PASS);
    mqtt.sSvrAddr = e2p_network.E2PtoString(E2PE_ADR_SVR_ADDR, E2PE_LEN_SVR_ADDR);
    mqtt.usSvrPort = e2p_network.read_16(E2PE_ADR_SVR_PORT);
    // for cal sewing or non-sewing mc
    oper.ucNonSewingMc = e2p_network.read(E2PE_ADR_NON_SEW_MC);
    if (oper.ucNonSewingMc > 1)
    {
        oper.ucNonSewingMc = 0; // sewing mc
        e2p_network.write(E2PE_ADR_NON_SEW_MC, oper.ucNonSewingMc);
    }
}

extern uint8_t ucCntUidRs;
void check_uid_blank_problem()
{
    if (nfc.ucLenUidBlank == 0)
        return;

    bool ret = false;
    for (uint8_t i = 0; i < nfc.ucLenUidBlank; i++)
    {
        if (nfc.sUidBlank.charAt(i) < 0x30)
            ret = true;
        if (i != 0 && i % 3 == 2 && nfc.sUidBlank.charAt(i) != ':')
            ret = true;

        // fix wrong character
        if (nfc.sUidBlank.charAt(i) < 0x30)
        {
            if (nfc.sUidBlank.charAt(i) < 0x20)
                nfc.sUidBlank.setCharAt(i, nfc.sUidBlank.charAt(i) + 0x20);
            else
                nfc.sUidBlank.setCharAt(i, nfc.sUidBlank.charAt(i) + 0x10);
        }

        if (i != 0 && i % 3 == 2 && nfc.sUidBlank.charAt(i) != ':')
            nfc.sUidBlank.setCharAt(i, ':');
    }

    if (ret)
    {
        debug.msg_debug.concat(" --/ check uid blank false => read again ");
        debug.msg_debug.concat(ucCntUidRs);
        debug.msg_debug.concat(" --/ aftern fix:  ");
        debug.msg_debug.concat("uid: ");
        debug.msg_debug.concat(nfc.sUidBlank);
        debug.send_debug = true;

        nfc.get_uid_blank_again = true;

        // write again
        e2p_info.StringToE2P(E2PE_ADR_NFC_UID_STR, nfc.ucLenUidBlank, nfc.sUidBlank);
        e2p_info.commit_flag = true;
    }
}

/////////////////////////////////////////////////////////////////////////////////////
void Fun_e2p::write_16(uint16_t usAddr, uint16_t usData)
{
    delay(1);
    EEPROM->write(usAddr, (uint8_t)(usData >> 8));
    delay(1);
    EEPROM->write(usAddr + 1, (uint8_t)usData);
}
/////////////////////////////////////////////////////////////////////////////////////
uint16_t Fun_e2p::read_16(uint16_t usAddr)
{
    uint16_t usRv = 0;
    usRv = (uint16_t)(EEPROM->read(usAddr) << 8);
    usRv += (uint16_t)EEPROM->read(usAddr + 1);
    return (usRv);
}
/////////////////////////////////////////////////////////////////////////////////////
void Fun_e2p::write_32(uint16_t usAddr, uint32_t ulData)
{
    uint8_t i;
    for (i = 0; i < 4; i++)
    {
        delay(1);
        EEPROM->write(usAddr + i, (uint8_t)(ulData >> (8 * (3 - i))));
    }
}
/////////////////////////////////////////////////////////////////////////////////////
uint32_t Fun_e2p::read_32(uint16_t usAddr)
{
    uint32_t usRv = 0;
    uint32_t val;
    uint8_t i;
    for (i = 0; i < 4; i++)
    {
        val = (uint32_t)EEPROM->read(usAddr + i);
        usRv += (uint32_t)(val << (8 * (3 - i)));
    }
    return (usRv);
}
/////////////////////////////////////////////////////////////////////////////////////
void Fun_e2p::write_buff(uint16_t usAddr, char *buff, uint8_t ucLen)
{
    uint16_t i = 0;
    for (i = usAddr; i < usAddr + ucLen; i++)
    {
        delay(1);
        EEPROM->write(i, buff[i - usAddr]);
    }
}
/////////////////////////////////////////////////////////////////////////////////////
void Fun_e2p::read_buff(uint16_t usAddr, char *buff, uint8_t ucLen)
{
    uint16_t i = 0;
    for (i = usAddr; i < usAddr + ucLen; i++)
        buff[i - usAddr] = EEPROM->read(i);
}
/////////////////////////////////////////////////////////////////////////////////////
void Fun_e2p::StringToE2P(uint16_t usAddr, uint8_t ucLen, String stStr)
{
    char cTmp[ucLen];

    stStr.toCharArray(cTmp, ucLen + 1);
    write_buff(usAddr, cTmp, ucLen);
}
/////////////////////////////////////////////////////////////////////////////////////
String Fun_e2p::E2PtoString(uint16_t usAddr, uint8_t ucLen)
{
    char cTmp[ucLen];
    String stString;

    read_buff(usAddr, cTmp, ucLen);

    stString = (char *)cTmp;

    // Erase grabage data(I don't know why.. some data happen..
    if (stString.length() > ucLen)
        stString.remove(ucLen, stString.length() - ucLen);

    return (stString);
}

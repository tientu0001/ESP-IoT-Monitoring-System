void updateTimeEEPROM()
{
    uint8_t ucHour, ucMin, ucSec;
    uint16_t usQRear, usQfront, usQfrontAddr;
    uint32_t ulDelta;

    usQfront = e2p_data.read_16(E2PE_ADR_DATA_Q_FRONT);
    usQRear = usQFontStart;

    if (usQfront == usQRear)
        return;

    ti.getSysTime();
    for (int i = usQRear; i < usQfront; i++)
    {
        usQfrontAddr = i * E2PE_LEN_Q_DATA;

        // if (e2p_data.read(usQfrontAddr + Q_BUF_IDX_YEAR) > 20)
        //     continue;

        ucHour = e2p_data.read(usQfrontAddr + Q_BUF_IDX_HOUR);
        ucMin = e2p_data.read(usQfrontAddr + Q_BUF_IDX_MIN);
        ucSec = e2p_data.read(usQfrontAddr + Q_BUF_IDX_SEC);

        ulDelta = (ucHourCur * 3600 + ucMinCur * 60 + ucSecCur) - (ucHour * 3600 + ucMin * 60 + ucSec);
        ucHour = ulDelta / 3600;
        ucMin = (ulDelta - ucHour * 3600) / 60;
        ucSec = ulDelta - ucHour * 3600 - ucMin * 60;

        if (ti.sysTime.tm_hour < ucHour) // calcute hour
            ucHour = ti.sysTime.tm_hour + 24 - ucHour;
        else
            ucHour = ti.sysTime.tm_hour - ucHour;

        if (ti.sysTime.tm_min < ucMin)
        { // calcute min
            ucHour--;
            ucMin = ti.sysTime.tm_min + 60 - ucMin;
        }
        else
            ucMin = ti.sysTime.tm_min - ucMin;
        if (ti.sysTime.tm_sec < ucSec)
        { // calcute second
            if (ucMin == 0)
            {
                ucHour--;
                ucMin = 59;
            }
            else
                ucMin--;
            ucSec = ti.sysTime.tm_sec + 60 - ucSec;
        }
        else
            ucSec = ti.sysTime.tm_sec - ucSec;

        ucHour = ucHour % 24;
        ucMin = ucMin % 60;
        ucSec = ucSec % 60;

        e2p_data.write(usQfrontAddr + Q_BUF_IDX_YEAR, ti.sysTime.tm_year - 2000);
        e2p_data.write(usQfrontAddr + Q_BUF_IDX_MON, ti.sysTime.tm_mon);
        e2p_data.write(usQfrontAddr + Q_BUF_IDX_DAY, ti.sysTime.tm_mday);
        e2p_data.write(usQfrontAddr + Q_BUF_IDX_HOUR, ucHour);
        e2p_data.write(usQfrontAddr + Q_BUF_IDX_MIN, ucMin);
        e2p_data.write(usQfrontAddr + Q_BUF_IDX_SEC, ucSec);
    }
    e2p_data.commit_flag = true;
}

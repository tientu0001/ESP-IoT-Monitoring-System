void init_e2p()
{
    e2p_info.begin(E2PE_ADR_BEGIN_INFO);
    delay(10);
    e2p_data.begin(E2PE_ADR_BEGIN_DATA);
    delay(10);
    e2p_network.begin(E2PE_ADR_BEGIN_NETWORK);
    delay(10);

    if (e2p_info.read(E2PE_ADR_INIT_INFO) != E2PE_INIT_CODE_INFO)
    {
        E2P_ResetE2p_info();
    }

    if (e2p_data.read(E2PE_ADR_INIT_DATA) != E2PE_INIT_CODE_DATA)
    {
        E2P_ResetE2p_data();
    }

    if (e2p_network.read(E2PE_ADR_INIT_NETWORK) != E2PE_INIT_CODE_NETWORK)
    {
        E2P_ResetE2p_network();
    }

    E2P_UpdateE2pVal();

    usQFontStart = e2p_data.read_16(E2PE_ADR_DATA_Q_FRONT);

    //////////////////////////////////////////
    //////////////////////////////////////////
    //////////////////////////////////////////
    //////////////////////////////////////////
}

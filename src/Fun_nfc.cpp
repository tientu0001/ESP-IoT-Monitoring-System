/////////////////////////////////////////////////////////////////////////////////////
uint8_t Fun_nfc::rfid_WriteTagData_ult()
{
    uint8_t i, j, ucBufTmp[18];
    uint8_t bPageAddr, bPageCnt;
    uint8_t ucTypeTagWrite;

    memset(ucDataBuf, 0x00, RFID_DATA_LEN);

    ucTypeTagWrite = ucWriteType;
    switch (ucWriteType)
    {
    case RFID_TAG_TYPE_NETWORK:
    {
        NFC_WRITE_TYPE_NETWORK();
        bPageCnt = RFID_PGE_END_NETWORK_ULT;
        break;
    }
    case RFID_TAG_TYPE_OUTPUT:
    {
        NFC_WRITE_TYPE_OUTPUT();
        bPageCnt = RFID_PGE_END_OUTPUT_ULT;
        break;
    }
    default:
        return NFC_TAG_COMM_ERROR;
    }

    // Write type to TAG
    bPageAddr = RFID_TAG_TYPE_INFO_PGE;
    ucBufTmp[0] = ucTypeTagWrite;
    ucBufTmp[1] = 0;
    ucBufTmp[2] = 0;
    ucBufTmp[3] = 0;
    RfidSts = (MFRC522::StatusCode)mfrc522.MIFARE_Write(bPageAddr++, ucBufTmp, 16);
    if (RfidSts != MFRC522::STATUS_OK)
        return (NFC_TAG_COMM_ERROR);

    // Write data to TAG
    j = 0;
    while (1)
    {
        for (i = 0; i < 4; i++)
            ucBufTmp[i] = ucDataBuf[(4 * j) + i];
        RfidSts = (MFRC522::StatusCode)mfrc522.MIFARE_Write(bPageAddr++, ucBufTmp, 16); // each time write 4 bytes(1 page addr) to nfc tag
        if (RfidSts != MFRC522::STATUS_OK)
            return (NFC_TAG_COMM_ERROR);

        j++;
        if (bPageAddr >= bPageCnt)
            break;
    }

    return (NFC_TAG_COMM_SUCCESS);
}
/////////////////////////////////////////////////////////////////////////////////////
uint8_t Fun_nfc::rfid_ReadTagData_ult()
{
    uint8_t ucTagType, i, j;
    uint8_t ucBufTmp[18];
    byte bPageAddr, bPageCnt;
    byte size = sizeof(ucBufTmp); // This library baced on Mifare classic(must use 18byte)

    memset(ucDataBuf, 0x00, RFID_DATA_LEN);

    // Check Type
    bPageAddr = RFID_TAG_TYPE_INFO_PGE;
    RfidSts = (MFRC522::StatusCode)mfrc522.MIFARE_Read(bPageAddr++, ucBufTmp, &size);
    if (RfidSts == MFRC522::STATUS_OK)
        ucTagType = ucBufTmp[0];
    else
        return NFC_TAG_COMM_ERROR;

    ucReadType = ucTagType & 0x0F; // for show data when tag successed

    switch (ucTagType & 0x0F)
    {

    case RFID_TAG_TYPE_NETWORK:
    {
        bPageCnt = RFID_PGE_END_NETWORK_ULT;
        break;
    }
    case RFID_TAG_TYPE_OUTPUT:
    {
        bPageCnt = RFID_PGE_END_OUTPUT_ULT;
        break;
    }
    default:
    {
        return NFC_TAG_TYPE_BLANK;
    }
    }

    // Read Nfc
    j = 0;
    while (1)
    {
        RfidSts = (MFRC522::StatusCode)mfrc522.MIFARE_Read(bPageAddr++, ucBufTmp, &size);
        for (i = 0; i < 4; i++)
            ucDataBuf[(4 * j) + i] = ucBufTmp[i];
        j++;
        if (RfidSts != MFRC522::STATUS_OK)
            return NFC_TAG_COMM_ERROR;
        if (bPageAddr >= bPageCnt)
            break;
    }

    // Action depends on Tag type
    switch (ucTagType & 0x0F)
    {
    case RFID_TAG_TYPE_NETWORK:
    {
        NFC_READ_TYPE_NETWORK();
        return NFC_TAG_TYPE_NETWORK;
    }
    case RFID_TAG_TYPE_OUTPUT:
    {
        FUN_NFC_READ_OUTPUT();
        return NFC_TAG_TYPE_OUTPUT;
    }
    default:
        break;
    }

    return NFC_TAG_COMM_ERROR;
}
/////////////////////////////////////////////////////////////////////////////////////
uint8_t Fun_nfc::rfid_WriteTagData_m1k()
{
    uint8_t ucBufTmp[18];
    byte bPageCnt, block;
    uint8_t ucTypeTagWrite;

    sTitle = "NFC Write";
    pcDispChar = (char *)"  (Write)";

    memset(ucDataBuf, 0x00, RFID_DATA_LEN);

    ucTypeTagWrite = ucWriteType;
    switch (ucWriteType)
    {
    case RFID_TAG_TYPE_NETWORK:
    {
#ifdef ESP_DEBUG_NFC
        Serial.printf("nfc_rfid_WriteTagData_m1k: NFC_WRITE_TYPE_NETWORK\r\n");
#endif
        NFC_WRITE_TYPE_NETWORK();
        bPageCnt = RFID_PGE_END_NETWORK_M1K;
        break;
    }
    case RFID_TAG_TYPE_OUTPUT:
    {
        NFC_WRITE_TYPE_OUTPUT();
        bPageCnt = RFID_PGE_END_OUTPUT_M1K;
        break;
    }
    case RFID_TAG_TYPE_EARSE:
    {
        bPageCnt = RFID_PGE_END_EARSE_M1K;
        break;
    }
    default:
        return NFC_TAG_COMM_ERROR;
    }

    block = 4;

    //    RfidSts = (MFRC522::StatusCode) mfrc522.PCD_Authenticate( MFRC522::PICC_CMD_MF_AUTH_KEY_A, block, &key, &(mfrc522.uid) );
    RfidSts = authenNfc1K(block);
    if (RfidSts != MFRC522::STATUS_OK)
    {
        pcDispChar = (char *)"  Fail Key";
        return NFC_TAG_COMM_ERROR;
    }

    // Write type to TAG
    ucBufTmp[0] = ucTypeTagWrite;
    for (uint8_t bf = 1; bf < 16; bf++)
        ucBufTmp[bf] = 0x00;
    RfidSts = (MFRC522::StatusCode)mfrc522.MIFARE_Write(block, ucBufTmp, 16);
    if (RfidSts != MFRC522::STATUS_OK)
        return NFC_TAG_COMM_ERROR;

    // Write data to TAG.. begin with block 5
    uint8_t blockzero = 5;
    uint8_t po = 0;

    for (byte block = blockzero; block < blockzero + bPageCnt; block++)
    {
        //  without get Block 0;3;7;11;15...
        if ((block == 7) || (block == 11) || (block == 15))
            continue;

        //    RfidSts = (MFRC522::StatusCode) mfrc522.PCD_Authenticate( MFRC522::PICC_CMD_MF_AUTH_KEY_A, block, &key, &(mfrc522.uid) );
        RfidSts = authenNfc1K(block);
        if (RfidSts != MFRC522::STATUS_OK)
        {
            pcDispChar = (char *)"  Fail Key";
            return NFC_TAG_COMM_ERROR;
        }

        // Write block
        RfidSts = (MFRC522::StatusCode)mfrc522.MIFARE_Write(block, &ucDataBuf[po * 16], 16);
        if (RfidSts != MFRC522::STATUS_OK)
            return NFC_TAG_COMM_ERROR;

        po++;
    }

    return NFC_TAG_COMM_SUCCESS;
}
/////////////////////////////////////////////////////////////////////////////////////
uint8_t Fun_nfc::rfid_ReadTagData_m1k()
{
    byte block;
    byte len;
    uint8_t ucTagType;
    uint8_t ucBufTmp[18];
    byte bPageCnt;

    sTitle = "NFC Read";
    pcDispChar = (char *)"   (Read)";

    memset(ucDataBuf, 0x00, RFID_DATA_LEN);

    //--- GET Typer NFC tag
    block = 4;
    len = 18;

    //    RfidSts = (MFRC522::StatusCode) mfrc522.PCD_Authenticate( MFRC522::PICC_CMD_MF_AUTH_KEY_A, block, &key, &(mfrc522.uid) );
    RfidSts = authenNfc1K(block);
    if (RfidSts != MFRC522::STATUS_OK)
    {
        pcDispChar = (char *)"  Fail Key";
        return NFC_TAG_COMM_ERROR;
    }

    RfidSts = (MFRC522::StatusCode)mfrc522.MIFARE_Read(block, ucBufTmp, &len);
    if (RfidSts == MFRC522::STATUS_OK)
        ucTagType = ucBufTmp[0];
    else
        return NFC_TAG_COMM_ERROR;

    switch (ucTagType & 0x0F)
    {
    case RFID_TAG_TYPE_NETWORK:
    {
        bPageCnt = RFID_PGE_END_NETWORK_M1K;
        break;
    }
    case RFID_TAG_TYPE_OUTPUT:
    {
        bPageCnt = RFID_PGE_END_OUTPUT_M1K;
        break;
    }
    default:
    {
        return NFC_TAG_TYPE_BLANK;
    }
    }

    byte blockzero = 5;
    uint8_t po = 0;

    for (byte block = blockzero; block < blockzero + bPageCnt; block++)
    {
        //  without get Block 0;3;7;11;15...
        if ((block == 7) || (block == 11) || (block == 15))
            continue;

        if (block == 8 || block == 12) // new sector, need to authen
        {
            //    RfidSts = (MFRC522::StatusCode) mfrc522.PCD_Authenticate( MFRC522::PICC_CMD_MF_AUTH_KEY_A, byte(block), &key, &(mfrc522.uid) );
            RfidSts = authenNfc1K(block);
            if (RfidSts != MFRC522::STATUS_OK)
            {
                pcDispChar = (char *)"  Fail Key";
                return NFC_TAG_COMM_ERROR;
            }
        }

        // Read block
        RfidSts = (MFRC522::StatusCode)mfrc522.MIFARE_Read(byte(block), &ucDataBuf[po * 16], &len);
        if (RfidSts != MFRC522::STATUS_OK)
            return NFC_TAG_COMM_ERROR;
        po++;
    }

    // Action depends on Tag type
    switch (ucTagType & 0x0F)
    {

    case RFID_TAG_TYPE_NETWORK:
    {
        NFC_READ_TYPE_NETWORK();
        return NFC_TAG_TYPE_NETWORK;
    }
    case RFID_TAG_TYPE_OUTPUT:
    {
        FUN_NFC_READ_OUTPUT();
        return NFC_TAG_TYPE_OUTPUT;
    }
    default:
        break;
    }

    return NFC_TAG_COMM_ERROR;
}
/////////////////////////////////////////////////////////////////////////////////////
void Fun_nfc::NFC_WRITE_TYPE_NETWORK()
{
    wifi.sSsid.toCharArray(ucSsid, E2PE_LEN_SSID + 1);
    wifi.sPassword.toCharArray(ucPassword, E2PE_LEN_PASS + 1);
    mqtt.sSvrAddr.toCharArray(ucSvrAddr, E2PE_LEN_SVR_ADDR + 1);

    int i, j;

    j = 0;
    for (i = j; i < j + E2PE_LEN_SSID; ++i)
        ucDataBuf[i] = ucSsid[i];
    j += E2PE_LEN_SSID;
    for (i = j; i < j + E2PE_LEN_PASS; ++i)
        ucDataBuf[i] = ucPassword[i - j];
    j += E2PE_LEN_PASS;
    for (i = j; i < j + E2PE_LEN_SVR_ADDR; ++i)
        ucDataBuf[i] = ucSvrAddr[i - j];
    j += E2PE_LEN_SVR_ADDR;
    ucDataBuf[i] = (uint8_t)(mqtt.usSvrPort >> 8);
    ucDataBuf[i + 1] = (uint8_t)mqtt.usSvrPort;
}
/////////////////////////////////////////////////////////////////////////////////////
void Fun_nfc::NFC_READ_TYPE_NETWORK()
{
    int i, j;

    j = 0;
    for (i = j; i < j + E2PE_LEN_SSID; ++i)
        ucSsid[i - j] = ucDataBuf[i]; // get ssid
    j += E2PE_LEN_SSID;
    for (i = j; i < j + E2PE_LEN_PASS; ++i)
        ucPassword[i - j] = ucDataBuf[i]; // get password
    j += E2PE_LEN_PASS;
    for (i = j; i < j + E2PE_LEN_SVR_ADDR; ++i)
        ucSvrAddr[i - j] = ucDataBuf[i]; // get svrAddr
    j += E2PE_LEN_SVR_ADDR;

    wifi.sSsid = String(ucSsid);
    wifi.sPassword = String(ucPassword);

    mqtt.sSvrAddr = String(ucSvrAddr);
    mqtt.usSvrPort = (uint16_t)ucDataBuf[i] << 8;
    mqtt.usSvrPort += (uint16_t)ucDataBuf[i + 1];

    e2p_network.StringToE2P(E2PE_ADR_SSID, E2PE_LEN_SSID, wifi.sSsid);
    e2p_network.StringToE2P(E2PE_ADR_PASS, E2PE_LEN_PASS, wifi.sPassword);
    e2p_network.StringToE2P(E2PE_ADR_SVR_ADDR, E2PE_LEN_SVR_ADDR, mqtt.sSvrAddr);
    e2p_network.write_16(E2PE_ADR_SVR_PORT, mqtt.usSvrPort);
    e2p_network.commit_flag = true;

#ifdef ESP_DEBUG_NFC
    Serial.printf("nfc_NFC_READ_TYPE_NETWORK\r\n");
    Serial.printf("\twifi.sSsid: %s\r\n", wifi.sSsid.c_str());
    Serial.printf("\twifi.sPassword: %s\r\n", wifi.sPassword.c_str());
    Serial.printf("\tmqtt.sSvrAddr: %s\r\n", mqtt.sSvrAddr.c_str());
    Serial.printf("\tmqtt.usSvrPort: %d\r\n", mqtt.usSvrPort);
#endif
}
/////////////////////////////////////////////////////////////////////////////////////
void Fun_nfc::NFC_WRITE_TYPE_OUTPUT()
{
    // reset data
    for (int i = 0; i < 4; i++)
        ucDataBuf[i] = 0x00;

    if (ucIncDecType == NFC_INCDEC_INCREASE)
    {
        ucDataBuf[0] = 0x02;
        ucDataBuf[2] = ucInDecScale;
    }
    else
    {
        ucDataBuf[1] = 0x02;
    }
}
/////////////////////////////////////////////////////////////////////////////////////
void Fun_nfc::FUN_NFC_READ_OUTPUT()
{
    for (int i = 0; i < 4; i++)
    {
        if (ucDataBuf[i] == 0x02 && i < 2)
        {
            if (i == 0) // Nfc increase output
            {
                if (ucDataBuf[i + 2] == 1)
                    oper.output = oper.output + 1;
                else
                    oper.output = oper.output + 5;

                oper.addQueue_product_data(Q_TYPE_OUTPUT, oper.output);

                lcd_refresh();
            }
            else // Nfc decrease output
            {
                if (oper.output)
                {
                    oper.output--;
                    oper.addQueue_product_data(Q_TYPE_OUTPUT, oper.output);

                    lcd_refresh();
                }
            }
        }
    }
}

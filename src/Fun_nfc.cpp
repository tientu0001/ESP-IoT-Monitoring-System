/////////////////////////////////////////////////////////////////////////////////////
String Fun_nfc::getUidNfcTag_reverse()
{
    String sWrkUid = "";
    String s;
    uint8_t ucIndx = 0;

    for (int i = mfrc522.uid.size - 1; i >= 0; i--)
    {
        ucUidBlank[ucIndx++] = mfrc522.uid.uidByte[i]; // add for save byte uid to E2P when detect blank nfc

        uint8_t ucUid = mfrc522.uid.uidByte[i];
        s = String(ucUid, HEX);
        if (s.length() == 1)
            s = '0' + s;
        sWrkUid += s;

        if (i != 0)
            sWrkUid += String(":");
    }

    sWrkUid.toUpperCase();
    return sWrkUid;
}
/////////////////////////////////////////////////////////////////////////////////////
void Fun_nfc::ckTAG_ID_UL()
{
    // Write mode
    if (ucMode == RFID_MOD_WRITE)
    {
        if (rfid_WriteTagData_ult() == NFC_TAG_COMM_ERROR)
        {
            ucMode = RFID_MOD_READ;

            nfc.bHanldeSuccess = false;
            active_led_nfc_fail();
        }
        else
        {
            nfc.bHanldeSuccess = true;
            active_led_nfc_success();
        }
    }
    // Read mode
    else if (ucMode == RFID_MOD_READ)
    {
        ucReadType = rfid_ReadTagData_ult(); // for show data when tag successed
        if (ucReadType == NFC_TAG_COMM_ERROR)
        {
            nfc.bHanldeSuccess = false;
            lcd.disp_nfc_tag_handle();
            active_led_nfc_fail();
        }
        else
        {
            if (ucReadType == NFC_TAG_TYPE_BLANK)
                handle_nfc_blank();

            lcd.disp_nfc_tag_read();
            active_led_nfc_success();
        }
    }
}

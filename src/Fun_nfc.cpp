static MFRC522::StatusCode authenNfc1K(byte bBlock)
{
    MFRC522::StatusCode StsCode;
    MFRC522::MIFARE_Key key;

    // authen with key B
    for (int k = 0; k < NR_KNOWN_KEYS; k++)
    {
        for (int i = 0; i < 6; i++)
            key.keyByte[i] = knownKeys[k][i];
        StsCode = mfrc522.PCD_Authenticate(MFRC522::PICC_CMD_MF_AUTH_KEY_B, bBlock, &key, &(mfrc522.uid)); // line 834 of MFRC522.cpp file
        if (StsCode == MFRC522::STATUS_OK)
            return StsCode;
    }

    return StsCode;
}
